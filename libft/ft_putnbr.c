#include "libft.h"

void	ft_putnbr(int n)
{
	int a[2];

	a[0] = 0;
	a[1] = 0;
	if (n == -2147483648)
	{
		ft_putnbr(-n / 10);
		ft_putnbr(8);
		return ;
	}
	if (n < 0)
	{
		ft_putchar('-');
		n = -n;
	}
	a[0] = n % 10;
	a[1] = n / 10;
	if (a[1] > 0)
		ft_putnbr(a[1]);
	ft_putchar(a[0] + '0');
}
